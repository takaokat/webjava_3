package jp.co.systena.tigerscave.kadai3.application.model;

public class ListForm {

  private int itemId;
  private int num;
  private int price;

  public int getItemId() {
    return this.itemId;
  }

  public void setItemId(int itemId) {
    this.itemId = itemId;
  }

  public int getNum(){
    return this.num;
  }

  public void setNum(int num){
    this.num = num;
  }

  public int getPrice(){
    return this.price;
  }

  public void setPrice(int price){
    this.price = price;
  }

}